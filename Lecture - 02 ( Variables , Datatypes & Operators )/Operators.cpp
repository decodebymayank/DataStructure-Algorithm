#include<iostream>
using namespace std;
int main()
{
   int num1 = 10 , num2 = 100;
   cout << "Sum is : " << num1+num2 << endl;
   cout << "Mult is : " << num1*num2 << endl;
   cout << "Diff is : " << num1-num2 << endl;
   cout << "Division is : " << num1/num2 << endl;
   cout << "Equal to Operator : " << (num1==num2) << endl;
   cout << "Logical Not : " << !(num1==num2) << endl;
   
}